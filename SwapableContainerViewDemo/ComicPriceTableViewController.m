#import "ComicPriceTableViewController.h"

@interface ComicPriceTableViewController ()

@property (strong, nonatomic) IBOutlet UITableView * tableView;

@end

@implementation ComicPriceTableViewController

- (void)viewWillAppear:(BOOL)animated
{
    [super viewWillAppear:animated];
    [self.tableView reloadData];
}

- (void)setComicData:(NSDictionary *)comicData
{
    _comicData = comicData;
    [self.tableView reloadData];
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [self.comicData[@"prices"] count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];
    
    // Configure the cell...
    NSDictionary * price = self.comicData[@"prices"][indexPath.row];
    
    cell.textLabel.text = price[@"type"];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"$%.2f", [price[@"price"] floatValue]];
    
    return cell;
}

